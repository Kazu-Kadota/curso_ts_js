/**
 * Aritmeticos
 * +: Adicao / Concatenacao
 * - / * 
 * **: Potencia
 * %: Resto da divisao
 * 
 * () -> ** -> * / % -> + -.
 */

const num1 = 5;
const str1 = `5`;
const num2 = 10;
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);
console.log(str1 + num2);

let contador = 1;
contador++;
++contador;
console.log(contador);
console.log(++contador);
console.log(--contador);